    print("  so wrapped phase in [-180, 180] is fine — no unwrapping needed.")

    # ------------------------------------------------------------------
    # Write CSV
    # ------------------------------------------------------------------
    out = Path("openloop_bode.csv")
    with out.open("w", newline="") as fh:
        w = csv.writer(fh)
        w.writerow(["freq_hz", "mag_db", "phase_deg"])
        for fi, mi, pi in zip(f, mag_ol, ph_ol):
            w.writerow([fi, mi, pi])
    print(f"\nWrote {len(f)} points → {out}")

    # ------------------------------------------------------------------
    # Plot: closed-loop vs open-loop + condition number
    # ------------------------------------------------------------------
    fig, axes = plt.subplots(3, 2, figsize=(14, 10), sharex=True)
    fig.suptitle("Closed-loop → Open-loop extraction  (L = T / (1−T))")

    ax = axes[0, 0]
    ax.semilogx(f, mag_cl)
    ax.set_ylabel("Magnitude (dB)")
    ax.set_title("Closed-loop T(jω)")
    ax.axhline(0, color="k", lw=0.5, ls="--")
    ax.grid(True, which="both", alpha=0.3)

    ax = axes[1, 0]
    ax.semilogx(f, ph_cl)
    ax.set_ylabel("Phase (deg)")
    ax.grid(True, which="both", alpha=0.3)

    # Condition number panel
    ax = axes[2, 0]
    ax.semilogx(f, cond, color="C3")
    ax.axhline(COND_WARN, color="k", lw=1, ls="--", label=f"warn threshold ({COND_WARN})")
    ax.set_ylabel("Cond = 1/|1−T|")
    ax.set_xlabel("Frequency (Hz)")
    ax.set_yscale("log")
    ax.legend(fontsize=8)
    ax.grid(True, which="both", alpha=0.3)

    ax = axes[0, 1]
    ax.semilogx(f, mag_ol)
    ax.set_ylabel("Magnitude (dB)")
    ax.set_title("Open-loop L(jω) = T/(1−T)")
    ax.axhline(0, color="k", lw=0.5, ls="--")
    ax.grid(True, which="both", alpha=0.3)
    if bad.any():
        ax.axvspan(f[bad].min(), f[bad].max(), alpha=0.15,
                   color="red", label=f"cond > {COND_WARN}")
        ax.legend(fontsize=8)

    ax = axes[1, 1]
    ax.semilogx(f, ph_ol)
    ax.set_ylabel("Phase (deg)")
    ax.grid(True, which="both", alpha=0.3)

    ax = axes[2, 1]
    ax.semilogx(f, mag_ol, label="open-loop mag")
    ax.axhline(0, color="k", lw=1, ls="--", label="0 dB")
    ax.set_ylabel("Magnitude (dB)")
    ax.set_xlabel("Frequency (Hz)")
    ax.set_ylim(-10, max(mag_ol.max() + 5, 10))
    ax.legend(fontsize=8)
    ax.grid(True, which="both", alpha=0.3)

    plt.tight_layout()
    plt.savefig("openloop_bode.png", dpi=120)
