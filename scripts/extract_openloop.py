def closed_to_open(f_hz, mag_db, phase_deg):
    """
    Invert T = L/(1+L) to get L = T/(1-T).

    Returns f_hz, ol_mag_db, ol_phase_deg, cond_number
    where cond_number = 1/|1-T|  (larger = more ill-conditioned).
    """
    T = 10.0 ** (mag_db / 20.0) * np.exp(1j * np.deg2rad(phase_deg))
    denom = 1.0 - T
    L = T / denom
    cond = 1.0 / np.abs(denom)
    ol_mag_db = 20.0 * np.log10(np.abs(L) + 1e-300)
    ol_phase_deg = np.rad2deg(np.angle(L))
    return f_hz, ol_mag_db, ol_phase_deg, cond


